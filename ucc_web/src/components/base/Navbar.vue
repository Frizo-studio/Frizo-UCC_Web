    handleScroll() {
      // 頁面滾動距頂部距離
      var scrollTop = window.pageYOffset;
      var scroll = scrollTop - this.i;
      this.i = scrollTop;

      if (window.location.pathname === "/chat") {
        window.removeEventListener("scroll", this.handleScroll, true);
      } else if (scroll < 0) {
        this.scrollUpOrDown = true;
      } else {
        this.scrollUpOrDown = false;
      }
    },

